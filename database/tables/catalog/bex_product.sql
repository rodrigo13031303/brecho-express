COMMENT ON COLUMN BEX_PRODUCT.CAT_ID IS 'Required category classification';
COMMENT ON COLUMN BEX_PRODUCT.BRD_ID IS 'Optional brand classification';
COMMENT ON COLUMN BEX_PRODUCT.PRD_TITLE IS 'Public product title';
COMMENT ON COLUMN BEX_PRODUCT.PRD_SLUG IS 'Canonical slug unique inside the store';
COMMENT ON COLUMN BEX_PRODUCT.PRD_DESCRIPTION IS 'Optional public description';
COMMENT ON COLUMN BEX_PRODUCT.PRD_PRICE IS 'Current nonnegative advertised price';
COMMENT ON COLUMN BEX_PRODUCT.PRD_QUANTITY IS 'Nonnegative advertised quantity';
COMMENT ON COLUMN BEX_PRODUCT.PRD_CONDITION IS 'Physical condition code';
COMMENT ON COLUMN BEX_PRODUCT.PRD_WEIGHT IS 'Optional positive weight';
COMMENT ON COLUMN BEX_PRODUCT.PRD_WIDTH IS 'Optional positive width';
COMMENT ON COLUMN BEX_PRODUCT.PRD_HEIGHT IS 'Optional positive height';
COMMENT ON COLUMN BEX_PRODUCT.PRD_LENGTH IS 'Optional positive length';
COMMENT ON COLUMN BEX_PRODUCT.PRD_STATUS IS 'Current product lifecycle status';
