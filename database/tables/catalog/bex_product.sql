DECLARE
  l_columns PLS_INTEGER;
  l_constraints PLS_INTEGER;
  l_indexes PLS_INTEGER;
BEGIN
  SELECT COUNT(*) INTO l_columns FROM USER_TAB_COLUMNS
   WHERE TABLE_NAME='BEX_PRODUCT';
  SELECT COUNT(*) INTO l_constraints FROM USER_CONSTRAINTS
   WHERE TABLE_NAME='BEX_PRODUCT'
     AND CONSTRAINT_NAME IN (
       'PK_PRODUCT','UK_PRODUCT_PUBLIC_ID','UK_PRODUCT_STORE_SLUG',
       'UK_PRODUCT_ID_STORE',
       'FK_PRD_STORE','FK_PRD_CATEGORY','FK_PRD_BRAND',
       'CK_PRD_PRICE','CK_PRD_QUANTITY','CK_PRD_CONDITION',
       'CK_PRD_WEIGHT','CK_PRD_DIMENSIONS_POSITIVE',
       'CK_PRD_DIMENSIONS_SET','CK_PRD_STATUS'
     );
  SELECT COUNT(*) INTO l_indexes FROM USER_INDEXES
   WHERE TABLE_NAME='BEX_PRODUCT'
     AND INDEX_NAME IN (
       'IDX_PRODUCT_CATEGORY','IDX_PRODUCT_BRAND',
       'IDX_PRODUCT_STATUS','IDX_PRODUCT_PRICE'
     );
  IF l_columns<>20 OR l_constraints<>14 OR l_indexes<>4 THEN
    RAISE_APPLICATION_ERROR(-20002,'BEX_PRODUCT physical validation failed');
  END IF;
END;
