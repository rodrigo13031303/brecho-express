CREATE INDEX IDX_CART_ITEM_STORE ON BEX_CART_ITEM(STR_ID);
CREATE INDEX IDX_CART_ITEM_STATUS ON BEX_CART_ITEM(CTI_STATUS);
CREATE UNIQUE INDEX UK_CART_ITEM_ACTIVE_PRODUCT ON BEX_CART_ITEM(
