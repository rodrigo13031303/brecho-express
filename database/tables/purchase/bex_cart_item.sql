CREATE TABLE BEX_CART_ITEM(
  CTI_ID NUMBER(19) GENERATED ALWAYS AS IDENTITY,
  CTI_PUBLIC_ID CHAR(32 CHAR) NOT NULL,
  CRT_ID NUMBER(19) NOT NULL,
  PRD_ID NUMBER(19) NOT NULL,
  STR_ID NUMBER(19) NOT NULL,
  CTI_QUANTITY NUMBER(12) NOT NULL,
  CTI_UNIT_PRICE NUMBER(12,2) NOT NULL,
  CTI_STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
  CTI_CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
  CTI_UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
  CTI_CREATED_BY NUMBER,
  CTI_UPDATED_BY NUMBER,
  CONSTRAINT PK_CART_ITEM PRIMARY KEY(CTI_ID),
  CONSTRAINT UK_CART_ITEM_PUBLIC_ID UNIQUE(CTI_PUBLIC_ID),
  CONSTRAINT FK_CTI_CART FOREIGN KEY(CRT_ID) REFERENCES BEX_CART(CRT_ID),
  CONSTRAINT FK_CTI_PRODUCT_STORE FOREIGN KEY(PRD_ID,STR_ID)
    REFERENCES BEX_PRODUCT(PRD_ID,STR_ID),
  CONSTRAINT CK_CTI_QUANTITY CHECK(CTI_QUANTITY>0),
  CONSTRAINT CK_CTI_PRICE CHECK(CTI_UNIT_PRICE>=0),
  CONSTRAINT CK_CTI_STATUS CHECK(CTI_STATUS IN('ACTIVE','REMOVED'))
);
